public class Node {
    private Node no;
    private Node next;
    public Node(Node no, Node next) {
        this.no = no;
        this.next = next;
    }
    public Node getNo() {
        return no;
    }
    public void setNo(Node no) {
        this.no = no;
    }
    public Node getNext() {
        return next;
    }
    public void setNext(Node next) {
        this.next = next;
    }

}
